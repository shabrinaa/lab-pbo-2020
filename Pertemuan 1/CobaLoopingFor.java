public class CobaLoopingFor{
    public static void main(String arg[]){
        for (int i=0; i<10; i++){
            
            System.out.print('*');
            System.out.println();
        }
    }
} 


/**
* Program looping for dipakai untuk perulangan yang telah diketahul pasti jumlahnya.
pada program diatas,dengan perintah for (int i=0; i<10; i++) akan dihasilkan 10 kali perulangan
dan dengan setiap baris jumlah bintang (*) terus bertambah 1 akibat perintah or (int j=0; j<i+1; j++).
*/